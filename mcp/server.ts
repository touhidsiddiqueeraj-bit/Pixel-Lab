// ---------------------------------------------------------------------------
// MCP JSON-RPC over stdio
// ---------------------------------------------------------------------------

interface JsonRpcRequest {
  jsonrpc: '2.0';
  id: string | number;
  method: string;
  params?: unknown;
}

interface JsonRpcResponse {
  jsonrpc: '2.0';
  id: string | number;
  result?: unknown;
  error?: { code: number; message: string; data?: unknown };
}

// Track the connected browser tab (only one at a time for simplicity).
let browserSocket: WebSocket | null = null;
const pendingCalls = new Map<string, { resolve: (val: unknown) => void; reject: (err: Error) => void; timeout: NodeJS.Timeout }>();

// ---------------------------------------------------------------------------
// WebSocket server (browser connects here)
// ---------------------------------------------------------------------------

const WS_PORT = 3004;
const wss = new WebSocketServer({ port: WS_PORT });

wss.on('connection', (ws) => {
  console.error(`[mcp] Browser tab connected from port ${WS_PORT}`);
  browserSocket = ws;

  ws.on('message', (data) => {
    try {
      const msg = JSON.parse(data.toString());
      // Browser sends back tool call results: { callId, success, result, error }
      if (msg.type === 'tool-result' && msg.callId) {
        const pending = pendingCalls.get(msg.callId);
        if (pending) {
          clearTimeout(pending.timeout);
          pendingCalls.delete(msg.callId);
          if (msg.success) {
            pending.resolve(msg.result);
          } else {
            pending.reject(new Error(msg.error || 'Tool call failed in browser'));
          }
        }
      } else if (msg.type === 'tools-list') {
        // Browser can send its live tool list — we could update TOOLS from it.
        // For now we just log.
        console.error(`[mcp] Browser reports ${msg.tools?.length || 0} tools available`);
      }
    } catch (e) {
      console.error('[mcp] Failed to parse browser message:', e);
    }
  });

  ws.on('close', () => {
    console.error('[mcp] Browser tab disconnected');
    browserSocket = null;
    // Reject all pending calls
    for (const [id, pending] of pendingCalls) {
      clearTimeout(pending.timeout);
      pending.reject(new Error('Browser tab disconnected'));
      pendingCalls.delete(id);
    }
  });

  ws.on('error', (err) => {
    console.error('[mcp] Browser socket error:', err);
  });
});

console.error(`[mcp] WebSocket server listening on ws://localhost:${WS_PORT}`);
console.error(`[mcp] Open Pixel Lab in a browser tab to connect.`);

// ---------------------------------------------------------------------------
// Forward a tool call to the browser and wait for the result
// ---------------------------------------------------------------------------

function callBrowserTool(name: string, args: Record<string, unknown>): Promise<unknown> {
  return new Promise((resolve, reject) => {
    if (!browserSocket || browserSocket.readyState !== WebSocket.OPEN) {
      reject(new Error('No browser tab connected. Open Pixel Lab in a browser and ensure the Automations panel is visible.'));
      return;
    }
    const callId = Math.random().toString(36).substring(2, 11);
    const timeout = setTimeout(() => {
      pendingCalls.delete(callId);
      reject(new Error(`Tool call "${name}" timed out after 30 seconds`));
    }, 30000);
    pendingCalls.set(callId, { resolve, reject, timeout });
    browserSocket.send(JSON.stringify({ type: 'tool-call', callId, name, args }));
  });
}

// ---------------------------------------------------------------------------
// MCP JSON-RPC handler (stdio)
// ---------------------------------------------------------------------------

function handleRequest(req: JsonRpcRequest): JsonRpcResponse | null {
  switch (req.method) {
    case 'initialize':
      return {
        jsonrpc: '2.0',
        id: req.id,
        result: {
          protocolVersion: '2024-11-05',
          capabilities: { tools: {} },
          serverInfo: { name: 'pixel-lab', version: '1.0.0' },
        },
      };

    case 'initialized':
      // Notification — no response needed
      return null;

    case 'tools/list':
      return {
        jsonrpc: '2.0',
        id: req.id,
        result: { tools: TOOLS },
      };

    case 'tools/call': {
      const params = req.params as { name: string; arguments?: Record<string, unknown> };
      const toolName = params.name;
      const toolArgs = params.arguments ?? {};
      // Forward to browser and wait for result
      const resultPromise = callBrowserTool(toolName, toolArgs);
      // Return a promise-based response — we'll write it to stdout when ready
      resultPromise
        .then((result) => {
          const response: JsonRpcResponse = {
            jsonrpc: '2.0',
            id: req.id,
            result: {
              content: [{
                type: 'text',
                text: JSON.stringify(result, null, 2),
              }],
              isError: false,
            },
          };
          process.stdout.write(JSON.stringify(response) + '\n');
        })
        .catch((err: Error) => {
          const response: JsonRpcResponse = {
            jsonrpc: '2.0',
            id: req.id,
            result: {
              content: [{ type: 'text', text: `Error: ${err.message}` }],
              isError: true,
            },
          };
          process.stdout.write(JSON.stringify(response) + '\n');
        });
      // Return null — the response will be written asynchronously
      return null;
    }

    default:
      return {
        jsonrpc: '2.0',
        id: req.id,
        error: { code: -32601, message: `Method not found: ${req.method}` },
      };
  }
}

// Read JSON-RPC from stdin, write responses to stdout
let inputBuffer = '';
process.stdin.setEncoding('utf8');
process.stdin.on('data', (chunk) => {
  inputBuffer += chunk;
  // Process line-delimited JSON
  let newlineIdx;
  while ((newlineIdx = inputBuffer.indexOf('\n')) >= 0) {
    const line = inputBuffer.slice(0, newlineIdx).trim();
    inputBuffer = inputBuffer.slice(newlineIdx + 1);
    if (!line) continue;
    try {
      const req = JSON.parse(line) as JsonRpcRequest;
      const response = handleRequest(req);
      if (response) {
        process.stdout.write(JSON.stringify(response) + '\n');
      }
    } catch (e) {
      console.error('[mcp] Failed to parse stdin line:', e);
    }
  }
});

process.stdin.on('end', () => {
  console.error('[mcp] stdin closed, shutting down');
  process.exit(0);
});

console.error('[mcp] MCP server ready. Waiting for client on stdin...');

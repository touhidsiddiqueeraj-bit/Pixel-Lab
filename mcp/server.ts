/**
 * MCP Server for Pixel Lab — WebSocket bridge.
 *
 * This is a local Node/Bun process that implements the Model Context Protocol
 * (MCP) over a WebSocket connection. It exposes Pixel Lab's editing tool set
 * (the same TOOL_DECLARATIONS + executeTool used by the AI agent) to external
 * MCP-compatible clients like Claude Desktop, Claude Code, or any agent
 * framework that speaks MCP.
 *
 * Architecture:
 *
 *   ┌─────────────────┐    stdio     ┌─────────────────┐    WebSocket    ┌─────────────────┐
 *   │  MCP Client     │◄────────────►│  This MCP       │◄───────────────►│  Pixel Lab      │
 *   │  (Claude, etc.) │   JSON-RPC   │  Server         │   tool calls    │  browser tab    │
 *   └─────────────────┘              │  (port 3004)    │   + responses   │  (executeTool)  │
 *                                    └─────────────────┘                  └─────────────────┘
 *
 * The MCP server speaks JSON-RPC 2.0 over stdio (the standard MCP transport)
 * with the MCP client, AND speaks raw JSON over a WebSocket (port 3004) with
 * the browser tab. When a tool call comes in from the MCP client, the server
 * forwards it to the browser via WebSocket, waits for the result, and sends
 * it back to the MCP client.
 *
 * The tool manifest is generated from TOOL_DECLARATIONS — the same source the
 * Gemini agent uses. This means any tool the agent can call, an external MCP
 * client can also call.
 *
 * Usage:
 *   bun run mcp/server.ts          # start the MCP server (stdio + WebSocket)
 *
 * The browser tab connects to ws://localhost:3004 automatically when the
 * AutomationsPanel is open (see mcp-client.ts).
 */

